function generateCurrentWeekShifts(): Shift[] {
  const today = new Date();
  const dayOfWeek = today.getDay();
  // Monday = 0 offset. getDay() returns 0 for Sunday, 1 for Monday, etc.
  const mondayOffset = dayOfWeek === 0 ? -6 : 1 - dayOfWeek;
  const monday = new Date(today);
  monday.setDate(today.getDate() + mondayOffset);

  const shifts: Shift[] = [];
  const weekDates: string[] = [];

  for (let i = 0; i < 7; i++) {
    const d = new Date(monday);
    d.setDate(monday.getDate() + i);
    weekDates.push(d.toISOString().split("T")[0]);
  }

  // Manager (staff1): always morning
  for (let i = 0; i < 7; i++) {
    const type = i === 6 ? "off" : "morning"; // Sunday off
    shifts.push({
      id: `shift-s1-${i}`,
      staffId: "staff1",
      date: weekDates[i],
      type: type as Shift["type"],
    });
  }

  // Waiter Marina (staff2): mixed shifts
  const marinaPattern: Shift["type"][] = [
    "morning",
    "morning",
    "afternoon",
    "morning",
    "afternoon",
    "morning",
    "off",
  ];
  for (let i = 0; i < 7; i++) {
    shifts.push({
      id: `shift-s2-${i}`,
      staffId: "staff2",
      date: weekDates[i],
      type: marinaPattern[i],
    });
  }

  // Waiter Thodoris (staff3): mixed shifts
  const thodorisPattern: Shift["type"][] = [
    "afternoon",
    "morning",
    "morning",
    "afternoon",
    "morning",
    "off",
    "afternoon",
  ];
  for (let i = 0; i < 7; i++) {
    shifts.push({
      id: `shift-s3-${i}`,
      staffId: "staff3",
      date: weekDates[i],
      type: thodorisPattern[i],
    });
  }

  // Barman Elisavet (staff4): afternoon shifts
  const elisavetPattern: Shift["type"][] = [
    "afternoon",
    "afternoon",
    "afternoon",
    "off",
    "afternoon",
    "afternoon",
    "afternoon",
  ];
  for (let i = 0; i < 7; i++) {
    shifts.push({
      id: `shift-s4-${i}`,
      staffId: "staff4",
      date: weekDates[i],
      type: elisavetPattern[i],
    });
  }

  // Chef Stefanos (staff5): alternating morning/afternoon
  const stefanosPattern: Shift["type"][] = [
    "morning",
    "afternoon",
    "morning",
    "afternoon",
    "morning",
    "morning",
    "off",
  ];
  for (let i = 0; i < 7; i++) {
    shifts.push({
      id: `shift-s5-${i}`,
      staffId: "staff5",
      date: weekDates[i],
      type: stefanosPattern[i],
    });
  }

  return shifts;
}

